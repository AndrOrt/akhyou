public class AlternateObjects{
    private String sourceName;
    private String link;
    private String dubbedOrSubbed;

    public String getSourceName() {
        return sourceName;
    }

    public void setSourceName(String sourceName) {
        this.sourceName = sourceName;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getDubbedOrSubbed() {
        return dubbedOrSubbed;
    }

    public void setDubbedOrSubbed(String dubbedOrSubbed) {
        this.dubbedOrSubbed = dubbedOrSubbed;
    }
}
